/**
 * Supported Look and Feels for meistercharts.
 */
@JsExport
enum class LookAndFeel {
  /**
   * The default look and feel. Uses the CSS font
   */
  Default,

  /**
   * Dark look and feel. Uses the CSS font.
   */
  Dark,

  /**
   * Look and feel for Neckar IT. Uses own fonts.
   */
  NeckarIT,

  /**
   * Debug look and feel
   */
  Debug,
}
